#include <iostream>
using namespace std;

int main() {
    int N;

    // Вводимо кількість секунд від початку доби
    cout << "Введіть кількість секунд від початку доби: ";
    cin >> N;

    // Перевірка на коректність введення
    if (N < 0) {
        cout << "Кількість секунд не може бути від'ємною!" << endl;
        return 1;
    }

    // Знаходимо кількість секунд, що пройшли з початку останньої години
    int seconds_in_last_hour = N % 3600;

    // Виводимо результат
    cout << "Кількість секунд, що пройшли з початку останньої години: " 
         << seconds_in_last_hour << endl;

    return 0;
}